/**
 * 链表
 */
public class Node<T> {
    /**
     * 节点数据
     */
    private T data;
    /**
     * 下一个节点
     */
    private Node<T> next;

    public Node(T data, Node<T> next) {
        this.data = data;
        this.next = next;
    }

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }

    public Node<T> getNext() {
        return next;
    }

    public void setNext(Node<T> next) {
        this.next = next;
    }

}
